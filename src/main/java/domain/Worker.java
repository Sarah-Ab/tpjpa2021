package domain;

public class Worker extends User {

    private String job;
    private int id;

    public Worker(){}

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
